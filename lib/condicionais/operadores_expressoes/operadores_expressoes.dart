void main(){
  // Operadores Aritméticos
  int a = 10;
  int b = 3;

  print('Soma: ${a + b}');          // Adição
  print('Subtração: ${a - b}');     // Subtração
  print('Multiplicação: ${a * b}'); // Multiplicação
  print('Divisão: ${a / b}');       // Divisão
  print('Módulo: ${a % b}');        // Resto da divisão

  // Operadores Relacionais
  print('Igualdade: ${a == b}');        // Igual a
  print('Diferença: ${a != b}');       // Diferente de
  print('Maior que: ${a > b}');        // Maior que
  print('Menor que: ${a < b}');        // Menor que
  print('Maior ou igual a: ${a >= b}');// Maior ou igual a
  print('Menor ou igual a: ${a <= b}');// Menor ou igual a

  // Operadores Lógicos
  // bool x = true;
  // bool y = false;

  // print('E lógico: ${x && y}'); // AND lógico
  // print('OU lógico: ${x || y}'); // OR lógico
  // print('Negação lógica: ${!x}'); // NOT lógico

  // Operadores de Atribuição
  int c = 5;
  c += 2; // c = c + 2
  print('Atribuição com soma: $c');

  c *= 3; // c = c * 3
  print('Atribuição com multiplicação: $c');



}